    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        // Check if a row is selected
        int selectedRow = table.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to edit.");
            return;
        }

        FormEdit f_edit = new FormEdit(this, true);
        // Populate the fields with data from the selected row
        f_edit.name.setText(table.getValueAt(selectedRow, 2).toString());
        f_edit.contact.setText(table.getValueAt(selectedRow, 3).toString());
        f_edit.address.setText(table.getValueAt(selectedRow, 4).toString());
        f_edit.gender.setSelectedItem(table.getValueAt(selectedRow, 5).toString());
        f_edit.setVisible(true);
    }//GEN-LAST:event_editActionPerformed

