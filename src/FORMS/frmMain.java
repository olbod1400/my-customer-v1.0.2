    boolean duplicateData(String name,String con,String add,String gen){
        boolean dup = false;
        int i = 0;
        while(i < table.getRowCount()){
            if(name.equals(table.getValueAt(i, 2) .toString()) &&
                    con.equals(table.getValueAt(i, 3) .toString())&& 
                    add.equals(table.getValueAt(i, 4) .toString()) &&
                    gen.equals(table.getValueAt(i, 5) .toString()) 
                    ){
                dup = true;
                break;
            }
            i++;
        }
        return dup;
    }
    
